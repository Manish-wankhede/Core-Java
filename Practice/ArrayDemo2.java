// Declaration & initialization in java

class ArrayDemo2
{
    public static void main(String args[])
    {
        int a[] = {10, 20, 30, 40, 50};

        System.out.print("\n Displaying element of array A = ");

        for( int i = 0 ; i < a.length ; i++ )
        {
            System.out.print(" "+a[i]);
        }
    }
}